-- H2 compatible table definitions for Fee Rate Management model
-- Source data tables (loaded from JSON files in data/fee/)

CREATE TABLE IF NOT EXISTS "FEE_TOLL_STATION" (
    "ID" VARCHAR(100) NOT NULL,
    "STATIONID" VARCHAR(20) NOT NULL,
    "NAME" VARCHAR(200),
    "TYPE" INTEGER,
    "TOLLPLAZACOUNT" INTEGER,
    "NEIGHBORID" VARCHAR(100),
    "OPERATION" INTEGER,
    "RECORDGENTIME" VARCHAR(50),
    "STATIONHEX" VARCHAR(20),
    "STATUS" VARCHAR(10),
    "USESTATUS" INTEGER,
    "REALTYPE" INTEGER,
    "REGIONNAME" VARCHAR(100),
    "COUNTRYNAME" VARCHAR(100),
    "REGIONALISMCODE" VARCHAR(20),
    "REGIONLISMCODE" VARCHAR(20),
    CONSTRAINT PK_FEE_TOLL_STATION PRIMARY KEY ("STATIONID")
);

