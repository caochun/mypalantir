-- Pipeline output tables

CREATE TABLE IF NOT EXISTS "FEE_CONTIGUITY" (
    "ID" BIGINT AUTO_INCREMENT NOT NULL,
    "EN_ROAD_NODE_ID" VARCHAR(20),
    "EN_ROAD_NODE_TYPE" INTEGER,
    "EX_ROAD_NODE_ID" VARCHAR(20),
    "EX_ROAD_NODE_TYPE" INTEGER,
    "MILES" INTEGER DEFAULT 0,
    "CHARGE_MILES" INTEGER DEFAULT 0,
    "INVALID" BOOLEAN DEFAULT FALSE,
    "RULE_ID" VARCHAR(10),
    "LASTVER" VARCHAR(20),
    CONSTRAINT PK_FEE_CONTIGUITY PRIMARY KEY ("ID")
);

CREATE TABLE IF NOT EXISTS "FEE_PROVINCE_RATE_PARAM" (
    "ID" BIGINT AUTO_INCREMENT NOT NULL,
    "TOLL_INTERVAL_ID" VARCHAR(20),
    "VEHICLE_TYPE" INTEGER,
    "FEE" INTEGER DEFAULT 0,
    "MFEE" INTEGER DEFAULT 0,
    "EFEE" INTEGER DEFAULT 0,
    "RATE_SOURCE" VARCHAR(20),
    "LASTVER" VARCHAR(20),
    CONSTRAINT PK_FEE_PROVINCE_RATE_PARAM PRIMARY KEY ("ID")
);

CREATE TABLE IF NOT EXISTS "FEE_MINIMUM_FEE_PATH" (
    "ID" BIGINT AUTO_INCREMENT NOT NULL,
    "EN_STATION_ID" VARCHAR(20),
    "EX_STATION_ID" VARCHAR(20),
    "VEHICLE_TYPE" INTEGER,
    "TOLL_INTERVALS_GROUP" VARCHAR(2000),
    "CHARGEFEE_GROUP" VARCHAR(2000),
    "CHARGEFEE95_GROUP" VARCHAR(2000),
    "TOTAL_FEE" INTEGER DEFAULT 0,
    "TOTAL_FEE95" INTEGER DEFAULT 0,
    "TOTAL_MILEAGE" INTEGER DEFAULT 0,
    "VALIDATION_FAILED" BOOLEAN DEFAULT FALSE,
    "LASTVER" VARCHAR(20),
    CONSTRAINT PK_FEE_MINIMUM_FEE_PATH PRIMARY KEY ("ID")
);

CREATE TABLE IF NOT EXISTS "FEE_VALIDATION_ERROR" (
    "ID" BIGINT AUTO_INCREMENT NOT NULL,
    "PATH_ID" BIGINT,
    "RULE_ID" VARCHAR(10),
    "MESSAGE" VARCHAR(2000),
    CONSTRAINT PK_FEE_VALIDATION_ERROR PRIMARY KEY ("ID")
);
