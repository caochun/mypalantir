    "STARTLNG" VARCHAR(20),
    "ENDLAT" VARCHAR(20),
    "ENDLNG" VARCHAR(20),
    "STARTSTAKENUM" VARCHAR(20),
    "ENDSTAKENUM" VARCHAR(20),
    "ACTUALLENGTH" VARCHAR(20),
    "CHARGELENGTH" VARCHAR(20),
    "DIRECTION" INTEGER,
    "PROVINCETYPE" INTEGER,
    "OPPOSITEID" VARCHAR(20),
    "GANTRYID" VARCHAR(30),
    "GANTRYNAME" VARCHAR(200),
    "TOLLFLAG" INTEGER,
    "TOLLROADFLAG" INTEGER,
    "TOLLROADS" VARCHAR(20),
    "ROADTYPE" VARCHAR(10),
